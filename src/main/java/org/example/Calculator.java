package org.example;

public class Calculator {





    public static int add(int firstValue, int secondValue){
        return firstValue + secondValue;
    }

    public static int multiply(int firstValue, int secondValue) {
        return firstValue * secondValue;
    }

    public static int subtraction(int firstValue, int secondValue) {
        return firstValue - secondValue;
    }

    public static int division(int firstValue, int secondValue) {
        return firstValue / secondValue;
    }
}
